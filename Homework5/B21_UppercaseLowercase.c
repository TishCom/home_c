#include <stdio.h>
#include <inttypes.h>

int main(int argc, char **argv)
{
	char charArray[50] = {0};
	
	int32_t i = 0;
	
	for (;charArray[i - 1] != '.'; i++)
		scanf("%c", &charArray[i]);
		
	for (int a = 0, b = i - 2; b >= 0; b--, a++)
	{
		switch (charArray[a])
		{
			case 'A':
			case 'B':
			case 'C':
			case 'D':
			case 'E':
			case 'F':
			case 'G':
			case 'H':
			case 'I':
			case 'J':
			case 'K':
			case 'L':
			case 'M':
			case 'N':
			case 'O':
			case 'P':
			case 'Q':
			case 'R':
			case 'S':
			case 'T':
			case 'U':
			case 'V':
			case 'W':
			case 'X':
			case 'Y':
			case 'Z':
				charArray[a] += 0x20;
		}
	}
	
	for (int a = 0; charArray[a] != '.'; a++)
		printf("%c", charArray[a]);


	return 0;
}

#include <stdio.h>
#include <inttypes.h>

int main(int argc, char **argv)
{
	char char1 = 0;
	
	scanf("%c", &char1);
	
	while (char1 != '.')
	{
		switch (char1)
		{
			case 'A':
			case 'B':
			case 'C':
			case 'D':
			case 'E':
			case 'F':
			case 'G':
			case 'H':
			case 'I':
			case 'J':
			case 'K':
			case 'L':
			case 'M':
			case 'N':
			case 'O':
			case 'P':
			case 'Q':
			case 'R':
			case 'S':
			case 'T':
			case 'U':
			case 'V':
			case 'W':
			case 'X':
			case 'Y':
			case 'Z':
				char1 += 'a' - 'A';
		}
		
		printf("%c", char1);
		
		scanf("%c", &char1);
	}

	return 0;
}
